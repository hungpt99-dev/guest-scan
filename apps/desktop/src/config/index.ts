let _initialized = false;

export function initializeConfig(): void {
  if (_initialized) return;
  _initialized = true;

  const envVars = getEnvVars();

  configureLogging({ level: envVars.logLevel as "DEBUG" | "INFO" | "WARN" | "ERROR" });
}
